let day = parseInt(prompt("Please enter a number 1-7"))

switch(day) {

case 1: "Sunday"
console.log("Sunday")
break;

case 2: "Monday"
console.log("Monday")
break;

case 3: "Tuesday"
console.log("Tuesday")
break;

case 4: "Wednesday"
console.log("Wednesday")
break;

case 5: "Thursday"
console.log("Thursday")
break;

case 6: "Friday"
console.log("Friday")
break;

case 7: "Saturday"
console.log("Saturday")
break;

default: 
    console.log("invalid day");
}
    